var color = 'red';
switch (color) {
    case 'blue':
        console.log('color is blue');
        break;
    case 'yellow':
        console.log('color is yellow');
        break;
    case 'green':
        console.log('color is green');
        break;
    case 'maroon':
        console.log('color is maroon');
        break;
    case 'black':
        console.log('color is black');
        break;
    case 'red':
        console.log('color is red');
        break;
}